/*
 * Calculates and prints the sum
 * of two numbers 12 and 5
 *
 * @author Maria Binia
 */

public class DataDemo {

    public static void main(String[] args) {
        // Initiation and declaration of variables
        int num1 = 12;
        int num2 = 5;
        int sum;

        // Commands
        sum = num1 + num2;

        // Print results
        System.out.println("The sum of 12 and 5 is = " + sum);
    }

}
